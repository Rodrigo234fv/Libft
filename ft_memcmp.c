#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	size_t		i;

	i = 0;
	if (!n || !str1 || !str2)
		return (0);
	while ((((char *)str1)[i] == ((char *)str2)[i]) && (i < n))
	{
		i++;
	}
	if (i >= n)
		return (0);
	return (((char *)str2)[i] - ((char *)str1)[i]);
}

